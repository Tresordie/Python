# 1.python中的列表类似于数组，列表只是一个高级集合(只需要知道名字和列表中数据项即可，其可容纳任何数据类型)
# 2.python中的列表中的数据类型不拘泥于某一种，可以是多种的混合
# 3.列表中的字符串数据需要用单引号或者双引号引起来，单个字符串元素既可以用单引号也可以用双引号
# 4.python中创建列表时，数据项是自下而上堆放的(形成一个堆栈)，堆栈中的第一个槽编号为0，第二个槽编号为1

